@dataclass(frozen=True, slots=True)
class ExportConfig:
    """Параметры каталога, имён и формата экспортируемых фотографий."""

    # Операторский параметр [Каталоги] «Готовые фотографии» из settings.ini.
    output_dir: Path

    # Операторский параметр [Сохранение] «Префикс имени» из settings.ini.
    # Текст перед последовательным номером в имени готовой фотографии.
    filename_prefix: str = "photo"

    # Операторский параметр [Сохранение] «Количество цифр» из settings.ini.
    # Минимальное количество цифр в последовательном номере.
    # Недостающие позиции заполняются нулями: при значении 4 — photo_0001.
    filename_digits: int = 4

    # Операторский параметр [Сохранение] «Формат» из settings.ini.
    # Допустимые значения: "jpg", "jpeg" и "png".
    # Значение "jpg" внутри программы преобразуется в "jpeg".
    output_format: str = "jpeg"

    # Операторский параметр [Сохранение] «Качество JPEG» из settings.ini.
    # Значение от 1 до 100; учитывается только при формате JPEG.
    jpeg_quality: int = 95

    def __post_init__(self) -> None:
        """Очистить префикс и проверить параметры экспорта."""
        normalized_prefix = self.filename_prefix.strip()
        object.__setattr__(self, "filename_prefix", normalized_prefix)

        if not normalized_prefix:
            raise ValueError(
                "Параметр «Префикс имени» (filename_prefix): после удаления "
                "пробелов по краям значение должно содержать хотя бы один символ"
            )
        forbidden = frozenset('<>:"/\\|?*')
        if any(character in forbidden for character in normalized_prefix):
            raise ValueError(
                "Параметр «Префикс имени» (filename_prefix): значение содержит "
                "запрещённый символ; запрещены < > : \" / \\ | ? *"
            )
        if self.filename_digits < 1:
            raise ValueError(
                "Параметр «Количество цифр» (filename_digits): значение должно "
                "быть положительным"
            )
        normalized_format = self.output_format.casefold()
        if normalized_format == "jpg":
            normalized_format = "jpeg"
        object.__setattr__(self, "output_format", normalized_format)

        if normalized_format not in {"jpeg", "png"}:
            raise ValueError(
                "Параметр «Формат» (output_format): допустимы значения jpg, "
                "jpeg и png"
            )
        if not 1 <= self.jpeg_quality <= 100:
            raise ValueError(
                "Параметр «Качество JPEG» (jpeg_quality): значение должно "
                "находиться в диапазоне [1, 100]"
            )

    @property
    def file_extension(self) -> str:
        """Вернуть расширение, соответствующее выбранному формату."""
        return ".jpg" if self.output_format == "jpeg" else ".png"


@dataclass(frozen=True, slots=True)
class DetectorConfig:
    """Входной каталог и внутренние параметры обнаружения фотографий."""

    # Операторский параметр [Каталоги] «Входные изображения» из settings.ini.
    input_dir: Path

    # Максимальная сторона уменьшенной копии в пикселях.
    # Копия используется только для поиска контуров; кадрирование выполняется
    # по исходному изображению полного разрешения.
    analysis_max_side: int = 1600

    # Глубина области вдоль всех четырёх краёв уменьшенной копии изображения,
    # из которой детектор берёт образцы цвета подложки. Значение задаётся
    # как доля короткой стороны копии. Например, при размере 1600 × 1200
    # и значении 0.04 анализируются полосы шириной 48 пикселей:
    # 1200 × 0.04 = 48. Эти полосы затем делятся на отдельные участки,
    # чтобы выбрать среди них однородные участки с преобладающим цветом.
    background_border_fraction: float = 0.04

    # Количество участков, на которые делится каждая сторона периметра.
    background_tiles_per_side: int = 12

    # Максимальный медианный разброс цветов LAB внутри участка, при котором
    # участок считается однородным фоном.
    background_tile_mad_max: float = 8.0

    # Максимальное расстояние между медианными цветами LAB участков,
    # позволяющее объединить их в одну цветовую группу фона.
    background_cluster_distance: float = 14.0

    # Доля пикселей, используемых для уточнения цвета подложки. Сначала цвет
    # подложки оценивается как медианный цвет отобранных участков периметра.
    # Затем для каждого пикселя вычисляется цветовое расстояние до этой оценки
    # в пространстве LAB. При значении 0.80 для нового медианного цвета берутся
    # ближайшие 80% пикселей, а наиболее отличающиеся 20% временно исключаются
    # как возможные тени, блики, загрязнения или фрагменты фотографии.
    # Отбор и пересчёт повторяются background_refinement_iterations раз.
    background_inlier_fraction: float = 0.80

    # Количество повторений отбора пикселей и пересчёта медианного цвета фона.
    background_refinement_iterations: int = 3

    # Минимальная доля участков периметра в доминирующей цветовой группе.
    # При меньшем покрытии создаётся предупреждение о неоднозначном фоне.
    background_min_cluster_fraction: float = 0.50

    # Нижняя граница цветового расстояния LAB между подложкой и фотографией.
    # Защищает автоматически рассчитанный порог от слишком малого значения.
    background_distance_min: float = 18.0

    # Коэффициент, определяющий допустимую неоднородность цвета подложки.
    # Для отобранных пикселей подложки вычисляются расстояния LAB до её
    # уточнённого цвета. Затем определяется медианное абсолютное отклонение
    # этих расстояний — оценка естественного разброса, мало чувствительная
    # к отдельным теням, бликам и загрязнениям. После пересчёта отклонения
    # в робастное стандартное отклонение итоговый порог рассчитывается как:
    # медианное расстояние + background_mad_multiplier × робастный разброс.
    # При значении 5.0 рассчитанный разброс умножается на 5, после чего
    # результат прибавляется к медианному расстоянию.
    # Увеличение значения допускает более неоднородную подложку, но повышает
    # риск принять похожие по цвету края фотографии за фон. Уменьшение делает
    # детектор чувствительнее к теням, бликам и шуму на самой подложке.
    background_mad_multiplier: float = 5.0

    # Значение рассчитанного порога, выше которого создаётся предупреждение
    # о неоднородном освещении или фоне.
    background_threshold_warning: float = 80.0

    # Размер квадратной области, которой OpenCV очищает чёрно-белую маску
    # найденных фотографий. Значение задаётся как доля короткой стороны
    # уменьшенной копии. Например, при размере 1600 × 1200 значение 0.008
    # даёт 1200 × 0.008 = 9.6 пикселя. Результат округляется, делается нечётным
    # и ограничивается снизу тремя пикселями; в этом примере получится ядро
    # 11 × 11 пикселей.
    # Сначала ядро закрывает небольшие отверстия и разрывы в областях
    # фотографий, затем удаляет отдельные мелкие пятна маски. Увеличение
    # значения очищает более крупные дефекты, но может соединить близко
    # лежащие фотографии или изменить их границы. Уменьшение лучше сохраняет
    # границы, но оставляет больше разрывов и мелкого шума.
    morph_kernel_fraction: float = 0.008

    # Минимальная доля кадра, которую должна занимать фотография. Контур —
    # замкнутая линия по границе найденной области. Детектор вычисляет площадь
    # области внутри внешнего контура на очищенной маске и делит её на площадь
    # всей уменьшенной копии. Проверка основана на площади внутри контура;
    # прямоугольная рамка строится позже. Значение 0.02 означает 2% площади
    # кадра или больше.
    min_photo_area_fraction: float = 0.02

    # Максимальная доля площади, рассчитанной тем же способом.
    # Значение 0.85 означает 85% площади кадра или меньше.
    max_photo_area_fraction: float = 0.85

    # Минимальная длина короткой стороны фотографии относительно короткой
    # стороны анализируемого кадра.
    min_photo_side_fraction: float = 0.08

    # Минимальное отношение площади контура к площади его прямоугольной рамки.
    # Чем ближе значение к 1.0, тем полнее контур заполняет прямоугольник.
    min_rectangularity: float = 0.55

    # Минимальное отношение длинной стороны рамки к короткой.
    min_aspect_ratio: float = 1.0

    # Максимальное отношение длинной стороны рамки к короткой.
    max_aspect_ratio: float = 2.25

    # Максимальное допустимое отклонение края фотографии от горизонтального
    # или вертикального направления, в градусах.
    # Контуры с большим наклоном отклоняются детектором.
    max_deskew_angle: float = 15.0

    def __post_init__(self) -> None:
        """Проверить внутренние пороги и диапазоны детектора."""
        self._validate_minimum_counts()
        self._validate_fraction_parameters()
        self._validate_positive_parameters()
        self._validate_related_limits()

    def _validate_minimum_counts(self) -> None:
        """Проверить минимальные размеры и количества операций детектора."""
        if self.analysis_max_side < 320:
            raise ValueError(
                "Параметр «Максимальная сторона копии для анализа» "
                "(analysis_max_side): значение должно быть больше или равно 320"
            )
        if self.background_tiles_per_side < 2:
            raise ValueError(
                "Параметр «Количество участков фона на каждой стороне» "
                "(background_tiles_per_side): значение должно быть больше "
                "или равно 2"
            )
        if self.background_refinement_iterations < 1:
            raise ValueError(
                "Параметр «Количество итераций уточнения фона» "
                "(background_refinement_iterations): значение должно быть "
                "положительным"
            )

    def _validate_fraction_parameters(self) -> None:
        """Проверить параметры, заданные долями от нуля до единицы."""
        fraction_parameters = {
            "background_border_fraction": "Доля края для оценки фона",
            "background_inlier_fraction": "Доля значений фона для уточнения",
            "background_min_cluster_fraction": (
                "Минимальная доля участков доминирующего фона"
            ),
            "morph_kernel_fraction": "Относительный размер морфологического ядра",
            "min_photo_area_fraction": "Минимальная доля площади фотографии",
            "max_photo_area_fraction": "Максимальная доля площади фотографии",
            "min_photo_side_fraction": "Минимальная доля короткой стороны",
            "min_rectangularity": "Минимальная прямоугольность контура",
        }
        for name, description in fraction_parameters.items():
            value = getattr(self, name)
            if not 0 < value <= 1:
                raise ValueError(
                    f"Параметр «{description}» ({name}): значение должно "
                    "быть больше 0 и меньше или равно 1"
                )

    def _validate_positive_parameters(self) -> None:
        """Проверить параметры, для которых допустимы положительные значения."""
        positive_parameters = {
            "background_tile_mad_max": "Максимальный разброс цвета участка фона",
            "background_cluster_distance": (
                "Цветовое расстояние объединения участков фона"
            ),
            "background_distance_min": "Минимальный порог отделения фона",
            "background_mad_multiplier": "Множитель разброса цвета фона",
            "background_threshold_warning": (
                "Порог предупреждения о неоднородном фоне"
            ),
        }
        for name, description in positive_parameters.items():
            if getattr(self, name) <= 0:
                raise ValueError(
                    f"Параметр «{description}» ({name}): значение должно быть "
                    "положительным"
                )

    def _validate_related_limits(self) -> None:
        """Проверить взаимные границы площади, пропорций и наклона."""
        if self.min_photo_area_fraction >= self.max_photo_area_fraction:
            raise ValueError(
                "Параметры «Минимальная доля площади фотографии» "
                "(min_photo_area_fraction) и «Максимальная доля площади "
                "фотографии» (max_photo_area_fraction): минимальное значение "
                "должно быть меньше максимального"
            )
        if self.min_aspect_ratio < 1:
            raise ValueError(
                "Параметр «Минимальное соотношение сторон» (min_aspect_ratio): "
                "значение должно быть больше или равно 1.0; получено "
                f"{self.min_aspect_ratio:g}"
            )
        if self.min_aspect_ratio > self.max_aspect_ratio:
            raise ValueError(
                "Значение параметра «Минимальное соотношение сторон» "
                "(min_aspect_ratio) должно быть меньше или равно значению "
                "параметра «Максимальное соотношение сторон» "
                "(max_aspect_ratio); получено соответственно "
                f"{self.min_aspect_ratio:g} и {self.max_aspect_ratio:g}"
            )
