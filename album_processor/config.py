"""Классы конфигурации и проверка параметров обработки CopyPhoto."""

from __future__ import annotations

from dataclasses import dataclass
from enum import Enum
from pathlib import Path


@dataclass(frozen=True, slots=True)
class CropperConfig:
    """Параметры полноразмерного кадрирования и геометрического выравнивания."""

    # Внутренний параметр алгоритма.
    # Число пикселей, добавляемых вокруг найденной рамки фотографии.
    # Запас оставляет место для поворота и перспективного выравнивания,
    # чтобы сохранить края и углы фотографии.
    safety_margin_pixels: int = 32

    # Внутренний параметр алгоритма. Максимальная глубина удаления подложки
    # от каждого края фотографии,
    # в пикселях полноразмерного изображения.
    substrate_trim_pixels: int = 24

    # Внутренний параметр алгоритма. Максимальное цветовое расстояние
    # в пространстве LAB, при котором
    # пиксель считается похожим на подложку.
    substrate_color_distance: float = 32.0

    # Внутренний параметр алгоритма. Минимальная доля пикселей подложки
    # в строке или столбце для их удаления.
    # Значение 0.75 означает, что подложкой должны быть 75% пикселей или больше.
    substrate_line_fraction: float = 0.75

    # Внутренний параметр алгоритма. Расстояние в пикселях, на которое
    # каждый найденный угол перемещается
    # по прямой в направлении центра фотографии. Общая длина перемещения
    # равна этому значению; изменения координат X и Y зависят от формы рамки.
    perspective_inset_pixels: int = 6

    # Внутренний параметр алгоритма. Максимальная разница координат
    # концов одного края в пикселях:
    # координат Y — для верхнего и нижнего краёв, координат X — для боковых.
    # Если каждая из четырёх разниц находится в диапазоне от 0 до указанного
    # порога, фотография извлекается прямоугольным срезом NumPy без
    # перспективного преобразования.
    perspective_bypass_max_edge_offset_pixels: float = 2.0

    # Операторский параметр [Обработка] «Поворачивать портретные в альбомные».
    # True поворачивает вертикальный результат длинной стороной по горизонтали.
    rotate_portrait_to_landscape: bool = True

    def __post_init__(self) -> None:
        """Проверить допустимость параметров кадрирования."""
        if self.safety_margin_pixels < 0:
            raise ValueError(
                "Параметр «Запас вокруг рамки» (safety_margin_pixels): "
                "значение должно быть больше или равно 0"
            )
        if self.substrate_trim_pixels < 0:
            raise ValueError(
                "Параметр «Глубина удаления подложки» (substrate_trim_pixels): "
                "значение должно быть больше или равно 0"
            )
        if self.substrate_color_distance <= 0:
            raise ValueError(
                "Параметр «Цветовое расстояние до подложки» "
                "(substrate_color_distance): значение должно быть положительным"
            )
        if not 0 < self.substrate_line_fraction <= 1:
            raise ValueError(
                "Параметр «Доля подложки в строке или столбце» "
                "(substrate_line_fraction): значение должно находиться "
                "в диапазоне (0, 1]"
            )
        if self.perspective_inset_pixels < 0:
            raise ValueError(
                "Параметр «Сдвиг углов к центру» (perspective_inset_pixels): "
                "значение должно быть больше или равно 0"
            )
        if self.perspective_bypass_max_edge_offset_pixels < 0:
            raise ValueError(
                "Параметр «Допустимое отклонение края для прямого кадрирования» "
                "(perspective_bypass_max_edge_offset_pixels): значение должно "
                "быть больше или равно 0"
            )


DEFAULT_CROPPER_CONFIG = CropperConfig()


class EnhancementMode(Enum):
    """Доступные оператору режимы первичной коррекции."""

    NONE = "Без коррекции"
    SOFT = "Мягкая"


@dataclass(frozen=True, slots=True)
class EnhancerConfig:
    """Режим и параметры первичной коррекции готового кадра."""

    # Операторский параметр [Обработка] «Режим коррекции».
    mode: EnhancementMode = EnhancementMode.NONE

    # Операторский параметр [Обработка] «Интенсивность коррекции».
    # Проценты из settings.ini преобразуются в долю от 0.0 до 1.0.
    intensity: float = 0.25

    # Внутренний параметр алгоритма. Ограничение усиления локального
    # контраста алгоритмом CLAHE.
    # Чем больше значение, тем сильнее могут подчёркиваться локальные различия
    # яркости, а вместе с ними шум и дефекты старой фотографии.
    clahe_clip_limit: float = 2.0

    # Внутренний параметр алгоритма. Количество участков по каждой стороне
    # изображения для локального анализа.
    # Значение 8 означает разбиение изображения на сетку 8 × 8 участков.
    # Чем больше значение, тем на меньших областях корректируется контраст.
    clahe_tile_grid_size: int = 8

    def __post_init__(self) -> None:
        """Проверить режим и числовые параметры первичной коррекции."""
        if not isinstance(self.mode, EnhancementMode):
            raise ValueError(
                "Параметр «Режим коррекции» (mode): допустимые значения — "
                "Без коррекции и Мягкая"
            )
        if not 0.0 <= self.intensity <= 1.0:
            raise ValueError(
                "Параметр «Интенсивность коррекции» (intensity): значение должно "
                "находиться в диапазоне от 0 до 1 включительно"
            )
        if self.clahe_clip_limit <= 0:
            raise ValueError(
                "Параметр «Ограничение локального контраста» (clahe_clip_limit): "
                "значение должно быть положительным"
            )
        if self.clahe_tile_grid_size < 2:
            raise ValueError(
                "Параметр «Размер сетки локальной коррекции» "
                "(clahe_tile_grid_size): значение должно быть больше или равно 2"
            )


DEFAULT_ENHANCER_CONFIG = EnhancerConfig()


